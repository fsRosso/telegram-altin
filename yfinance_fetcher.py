logger = logging.getLogger(__name__)

class YFinanceFetcher:
    """yfinance ile fiyat √ßekme ve doƒürulama"""
    
    def __init__(self):
        # Ticker sembolleri
        self.gold_ticker = "GC=F"  # XAUUSD (Altƒ±n)
        self.usd_rub_ticker = "USDRUB=X"  # USD/RUB d√∂viz kuru
        
    def get_xauusd_price(self) -> Optional[float]:
        """XAUUSD (Altƒ±n) fiyatƒ±nƒ± √ßeker"""
        try:
            # Rate limiting i√ßin kƒ±sa bekleme
            import time
            time.sleep(0.5)  # 500ms bekle
            
            gold = yf.Ticker(self.gold_ticker)
            
            # √ñnce info metodunu dene
            try:
                info = gold.info
                if info and 'regularMarketPrice' in info:
                    price = info['regularMarketPrice']
                    if price:
                        logger.info(f"‚úÖ yfinance XAUUSD (info): ${price:.2f}")
                        return float(price)
            except Exception as e:
                logger.warning(f"‚ö†Ô∏è yfinance info hatasƒ±: {e}")
            
            # info √ßalƒ±≈ümazsa history metodunu dene
            try:
                hist = gold.history(period="1d")
                if not hist.empty:
                    price = hist['Close'].iloc[-1]
                    logger.info(f"‚úÖ yfinance XAUUSD (history): ${price:.2f}")
                    return float(price)
            except Exception as e:
                logger.warning(f"‚ö†Ô∏è yfinance history hatasƒ±: {e}")
            
            logger.warning("‚ö†Ô∏è yfinance XAUUSD verisi bulunamadƒ±")
            return None
                
        except Exception as e:
            logger.error(f"‚ùå yfinance XAUUSD hatasƒ±: {e}")
            return None
    
    def get_usd_rub_rate(self) -> Optional[float]:
        """USD/RUB d√∂viz kurunu √ßeker"""
        try:
            # Rate limiting i√ßin kƒ±sa bekleme
            import time
            time.sleep(0.5)  # 500ms bekle
            
            usd_rub = yf.Ticker(self.usd_rub_ticker)
            
            # √ñnce info metodunu dene
            try:
                info = usd_rub.info
                if info and 'regularMarketPrice' in info:
                    rate = info['regularMarketPrice']
                    if rate:
                        logger.info(f"‚úÖ yfinance USD/RUB (info): {rate:.4f}")
                        return float(rate)
            except Exception as e:
                logger.warning(f"‚ö†Ô∏è yfinance USD/RUB info hatasƒ±: {e}")
            
            # info √ßalƒ±≈ümazsa history metodunu dene
            try:
                hist = usd_rub.history(period="1d")
                if not hist.empty:
                    rate = hist['Close'].iloc[-1]
                    logger.info(f"‚úÖ yfinance USD/RUB (history): {rate:.4f}")
                    return float(rate)
            except Exception as e:
                logger.warning(f"‚ö†Ô∏è yfinance USD/RUB history hatasƒ±: {e}")
            
            logger.warning("‚ö†Ô∏è yfinance USD/RUB verisi bulunamadƒ±")
            return None
                
        except Exception as e:
            logger.error(f"‚ùå yfinance USD/RUB hatasƒ±: {e}")
            return None
    
    def calculate_xaurub_gram_price(self) -> Optional[float]:
        """XAUUSD ve USD/RUB'den XAURUB gram fiyatƒ±nƒ± hesaplar (√∑31.1034768)"""
        try:
            xauusd = self.get_xauusd_price()
            usd_rub = self.get_usd_rub_rate()
            
            if xauusd and usd_rub:
                # XAUUSD √ó USD/RUB √∑ 31.1034768 = Gram fiyatƒ±
                calculated_xaurub = xauusd * usd_rub
                gram_price = calculated_xaurub / 31.1034768
                logger.info(f"‚úÖ Hesaplanan XAURUB gram fiyatƒ±: {gram_price:.4f} RUB/gram")
                return gram_price
            else:
                logger.warning("‚ö†Ô∏è XAURUB gram fiyatƒ± hesaplanamadƒ± - eksik veri")
                return None
                
        except Exception as e:
            logger.error(f"‚ùå XAURUB gram fiyatƒ± hesaplama hatasƒ±: {e}")
            return None
    
    def calculate_xaurub_from_components(self) -> Optional[float]:
        """XAUUSD ve USD/RUB'den XAURUB hesaplar (eski metod - geriye uyumluluk i√ßin)"""
        try:
            xauusd = self.get_xauusd_price()
            usd_rub = self.get_usd_rub_rate()
            
            if xauusd and usd_rub:
                calculated_xaurub = xauusd * usd_rub
                logger.info(f"‚úÖ Hesaplanan XAURUB: {calculated_xaurub:.2f} RUB")
                return calculated_xaurub
            else:
                logger.warning("‚ö†Ô∏è XAURUB hesaplanamadƒ± - eksik veri")
                return None
                
        except Exception as e:
            logger.error(f"‚ùå XAURUB hesaplama hatasƒ±: {e}")
            return None
    
    def validate_xaurub_price(self, direct_xaurub: float, tolerance_percent: float = None) -> Dict:
        """Direkt XAURUB ile hesaplanan XAURUB'yi kar≈üƒ±la≈ütƒ±rƒ±r"""
        try:
            if tolerance_percent is None:
                tolerance_percent = PRICE_VALIDATION_TOLERANCE
                
            calculated_xaurub = self.calculate_xaurub_from_components()
            
            if calculated_xaurub is None:
                return {
                    "valid": False,
                    "error": "Hesaplanan XAURUB bulunamadƒ±",
                    "direct_price": direct_xaurub,
                    "calculated_price": None,
                    "difference_percent": None
                }
            
            # Fark y√ºzdesi hesapla
            difference = abs(direct_xaurub - calculated_xaurub)
            difference_percent = (difference / calculated_xaurub) * 100
            
            # Tolerans kontrol√º
            is_valid = difference_percent <= tolerance_percent
            
            result = {
                "valid": is_valid,
                "direct_price": direct_xaurub,
                "calculated_price": calculated_xaurub,
                "difference": difference,
                "difference_percent": difference_percent,
                "tolerance_percent": tolerance_percent,
                "status": "‚úÖ Normal" if is_valid else "‚ö†Ô∏è Anormal"
            }
            
            if is_valid:
                logger.info(f"‚úÖ XAURUB doƒürulama ba≈üarƒ±lƒ±: Fark %{difference_percent:.2f}")
            else:
                logger.warning(f"‚ö†Ô∏è XAURUB anormallik tespit edildi: Fark %{difference_percent:.2f}")
            
            return result
            
        except Exception as e:
            logger.error(f"‚ùå XAURUB doƒürulama hatasƒ±: {e}")
            return {
                "valid": False,
                "error": str(e),
                "direct_price": direct_xaurub,
                "calculated_price": None,
                "difference_percent": None
            }
    
    def get_market_status(self) -> Dict:
        """Piyasa durumu √∂zeti"""
        try:
            xauusd = self.get_xauusd_price()
            usd_rub = self.get_usd_rub_rate()
            
            status = {
                "xauusd": xauusd,
                "usd_rub": usd_rub,
                "calculated_xaurub": None,
                "timestamp": "≈ûimdi"
            }
            
            if xauusd and usd_rub:
                status["calculated_xaurub"] = xauusd * usd_rub
            
            return status
            
        except Exception as e:
            logger.error(f"‚ùå Piyasa durumu hatasƒ±: {e}")
            return {"error": str(e)}
    
    def get_detailed_info(self) -> Dict:
        """Detaylƒ± piyasa bilgileri"""
        try:
            gold = yf.Ticker(self.gold_ticker)
            usd_rub = yf.Ticker(self.usd_rub_ticker)
            
            # G√ºvenli info alma
            try:
                gold_info = gold.info
            except:
                gold_info = {}
                
            try:
                usd_rub_info = usd_rub.info
            except:
                usd_rub_info = {}
            
            detailed_info = {
                "gold": {
                    "price": gold_info.get('regularMarketPrice'),
                    "change": gold_info.get('regularMarketChange'),
                    "change_percent": gold_info.get('regularMarketChangePercent'),
                    "volume": gold_info.get('volume'),
                    "market_cap": gold_info.get('marketCap'),
                    "previous_close": gold_info.get('previousClose'),
                    "open": gold_info.get('regularMarketOpen'),
                    "day_high": gold_info.get('dayHigh'),
                    "day_low": gold_info.get('dayLow')
                },
                "usd_rub": {
                    "rate": usd_rub_info.get('regularMarketPrice'),
                    "change": usd_rub_info.get('regularMarketChange'),
                    "change_percent": usd_rub_info.get('regularMarketChangePercent'),
                    "previous_close": usd_rub_info.get('previousClose')
                }
            }
            
            return detailed_info
            
        except Exception as e:
            logger.error(f"‚ùå Detaylƒ± bilgi hatasƒ±: {e}")
            return {"error": str(e)}

